package gopi;

public class Shift {
	public static void main(String[] args) {
		System.out.println("....Left Shift....");
		int a=13;
		System.out.println(a<<1);
		System.out.println(a<<2);
		System.out.println(a<<3);
		System.out.println("....Right Shift....");
		System.out.println(a>>1);
		System.out.println(a>>2);
		System.out.println(a>>3);
	}
}
